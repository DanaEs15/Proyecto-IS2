    private void txtTelefonoModificaClienteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelefonoModificaClienteKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();

        if((c<'0' || c>'9') && (c != KeyEvent.VK_BACK_SPACE)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Solo se aceptan números enteros"
                + " positvos...!!!", "", JOptionPane.WARNING_MESSAGE);
        }
        if(txtTelefonoModificaCliente.getText().length()==10){
            evt.consume();
            JOptionPane.showMessageDialog(null, "El TELÉFONO solo debe "
                + "tener máximo 10 dígitos!!!", "", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtTelefonoModificaClienteKeyTyped

    private void btnGuadarModificaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuadarModificaClienteActionPerformed
        // TODO add your handling code here:
        Cliente c;
        String _cedula = ProyectoIS2._Ced; 
        boolean pass = false, pass2 = false, pass3 = false, pass4 = false;

        //Validaciones del campo de texto Nombres
        do{
            if(txtNombreModificaCliente.getText().equals("")){
                JOptionPane.showMessageDialog(null,"El campo NOMBRES no debe estar vacío...!!!",
                    "",JOptionPane.WARNING_MESSAGE);
                txtNombreModificaCliente.requestFocus();
                return;
            }
            else{
                pass = true;
            }
        }while(!pass);

        //Validaciones del campo de texto Apellidos
        do{
            if(txtApellidosModificaCliente.getText().equals("")){
                JOptionPane.showMessageDialog(null,"El campo APELLIDOS no debe estar vacío...!!!",
                    "",JOptionPane.WARNING_MESSAGE);
                txtApellidosModificaCliente.requestFocus();
                return;
            }
            else{
                pass2 = true;
            }
        }while(!pass2);

        //Validaciones del campo de texto Dirección
        do{
            if(txtDireccionModificaCliente.getText().equals("")){
                JOptionPane.showMessageDialog(null,"El campo DIRECCIÓN no debe estar vacío...!!!",
                    "",JOptionPane.WARNING_MESSAGE);
                txtDireccionModificaCliente.requestFocus();
                return;
            }            
            else{
                pass3 = true;
            }
        }while(!pass3);
        
        //Validaciones del campo de texto Teléfono
        do{
            if(txtTelefonoModificaCliente.getText().equals("")){
                JOptionPane.showMessageDialog(null,"El campo TELÉFONO no debe estar vacío...!!!",
                    "",JOptionPane.WARNING_MESSAGE);
                txtTelefonoModificaCliente.requestFocus();
                return;
            }
            else if(txtTelefonoModificaCliente.getText().length()<9){
                JOptionPane.showMessageDialog(null,"EL TELÉFONO debe tener al menos 9 dígitos...!!!"
                    ,"",JOptionPane.WARNING_MESSAGE);
                //txtClienteTelefono.setText("");
                txtTelefonoModificaCliente.requestFocus();
                return;
            }
            else{
                pass4 = true;
            }
        }while(!pass4);

        c = new Cliente("", txtNombreModificaCliente.getText(),
            txtApellidosModificaCliente.getText(), txtDireccionModificaCliente.getText(),
            txtTelefonoModificaCliente.getText());
        try {
            c.modificaCliente(_cedula);
        } catch (Exception ex) {

        }
        Limpiar();
        JOptionPane.showMessageDialog(null, "Datos de CLIENTE modificados con éxito!!!"
            , "", JOptionPane.INFORMATION_MESSAGE);
        setVisible(false);
    }//GEN-LAST:event_btnGuadarModificaClienteActionPerformed

