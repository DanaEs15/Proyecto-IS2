    private void RecuperaDatos(){
        try{
            AccesoBD bd = new AccesoBD();
            bd.conectaBD();
            String sql = "SELECT nombres, apellidos, direccion, telefono FROM cliente "
                + "WHERE cedula = '" + ProyectoIS2._Ced + "'"; 
            Statement sta = bd.getConexion().createStatement();
            ResultSet rs= sta.executeQuery(sql);

            while(rs.next()){
                txtNombreModificaCliente.setText(rs.getString("nombres"));
                txtApellidosModificaCliente.setText(rs.getString("apellidos"));
                txtDireccionModificaCliente.setText(rs.getString("direccion"));
                txtTelefonoModificaCliente.setText(rs.getString("telefono"));
            }
                
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }

