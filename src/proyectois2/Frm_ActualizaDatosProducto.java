    private void bntModificaProductosGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntModificaProductosGuardarActionPerformed
        // TODO add your handling code here:
        String _codigo = ProyectoIS2._Code;
        Articulo a = new Articulo();
        boolean pass = false, pass2 = false, pass3 = false, pass4 = false;

        //Validaciones del campo de texto Cantidad
        do{
            if(txtModificaStockProducto.getText().equals("")){
                JOptionPane.showMessageDialog(null,"El campo CANTIDAD no debe estar vacío...!!!",
                    "",JOptionPane.WARNING_MESSAGE);
                txtModificaStockProducto.requestFocus();
                return;
            }
            else if(Integer.parseInt(txtModificaStockProducto.getText())<=0){
                JOptionPane.showMessageDialog(null,"La CANTIDAD no puede ser 0...!!!"
                    ,"",JOptionPane.WARNING_MESSAGE);
                txtModificaStockProducto.setText("");
                txtModificaStockProducto.requestFocus();
                return;
            }
            else{
                pass2 = true;
            }
        }while(!pass2);
        
        //Validaciones del campo de texto Descripción
        do{
            if(txtModificaNombreProducto.getText().equals("")){
                JOptionPane.showMessageDialog(null,"El campo DESCRIPCIÓN no debe estar vacío...!!!",
                    "",JOptionPane.WARNING_MESSAGE);
                txtModificaNombreProducto.requestFocus();
                return;
            }
            else{
                pass3 = true;
            }
        }while(!pass3);

        //Validaciones del campo de texto Precio
        do{
            if(txtModificaCostoProducto.getText().equals("")){
                JOptionPane.showMessageDialog(null,"El campo PRECIO no debe estar vacío...!!!",
                    "",JOptionPane.WARNING_MESSAGE);
                txtModificaCostoProducto.requestFocus();
                return;
            }
            else if(Double.parseDouble(txtModificaCostoProducto.getText())<=0){
                JOptionPane.showMessageDialog(null,"El PRECIO no puede ser 0...!!!"
                    ,"",JOptionPane.WARNING_MESSAGE);
                txtModificaCostoProducto.setText("");
                txtModificaCostoProducto.requestFocus();
                return;
            }
            else{
                pass4 = true;
            }
        }while(!pass4); 

        a = new Articulo("",Integer.parseInt(txtModificaStockProducto.getText()),0,
            txtModificaNombreProducto.getText(), Double.parseDouble(txtModificaCostoProducto.getText()));
        try {
            a.modificaProducto(_codigo);
        } catch (Exception ex) {
            System.out.println(ex.toString());
        }
        Limpiar();
        JOptionPane.showMessageDialog(null, "Datos del PRODUCTO modificado con éxito!!!"
            , "", JOptionPane.INFORMATION_MESSAGE);
        setVisible(false);
    }//GEN-LAST:event_bntModificaProductosGuardarActionPerformed

