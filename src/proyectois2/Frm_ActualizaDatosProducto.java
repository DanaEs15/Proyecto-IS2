    private void RecuperaDatos(){
        try{
            AccesoBD bd = new AccesoBD();
            bd.conectaBD();
            String sql = "SELECT stock, descripcion, precio FROM articulo "
                + "WHERE codigo = '" + ProyectoIS2._Code + "'"; 
            Statement sta = bd.getConexion().createStatement();
            ResultSet rs= sta.executeQuery(sql);

            while(rs.next()){
                txtModificaNombreProducto.setText(rs.getString("descripcion"));
                txtModificaStockProducto.setText(rs.getString("stock"));
                txtModificaCostoProducto.setText(rs.getString("precio"));
            }
                
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString(), "", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }
    
    public void Limpiar(){
        txtModificaNombreProducto.setText("");
        txtModificaCostoProducto.setText("");
        txtModificaStockProducto.setText("");       
    }
    
