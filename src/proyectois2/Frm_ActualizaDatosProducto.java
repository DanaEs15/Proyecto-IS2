    private void txtModificaCostoProductoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtModificaCostoProductoKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();

        if((c<'0' || c>'9') && (c != KeyEvent.VK_BACK_SPACE) && (c != '.'||
            txtModificaCostoProducto.getText().contains("."))){
        evt.consume();
        JOptionPane.showMessageDialog(null, "Solo se aceptan numeros enteros "
            + "positivos y un punto decimal !!!","",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtModificaCostoProductoKeyTyped

