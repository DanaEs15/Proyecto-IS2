    private void txtBuscaProductoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaProductoKeyPressed
        // TODO add your handling code here:
        AccesoBD bd = new AccesoBD();
        String[] Titulos = {"Código", "Cantidad", "Descripcion", "Precio"};
        String[] Registro = new String[50];

        String sql = "SELECT * FROM articulo WHERE codigo LIKE '%" + txtBuscaProducto.getText()+ "%'"
        + "OR cantidad LIKE '%" + txtBuscaProducto.getText() + "%'"
        + "OR descripcion LIKE '%" + txtBuscaProducto.getText() + "%'"
        + "OR precio LIKE '%" + txtBuscaProducto.getText() + "%'";

        model = new DefaultTableModel(null,Titulos);
        try {
            bd.conectaBD();
            Statement sta = bd.getConexion().createStatement();
            ResultSet rs= sta.executeQuery(sql);

            while(rs.next()){
                Registro[0] = rs.getString("codigo");
                Registro[1] = rs.getString("cantidad");
                Registro[2] = rs.getString("descripcion");
                Registro[3] = rs.getString("precio");
                model.addRow(Registro);
            }
            jtMuestraProductos.setModel(model);
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_txtBuscaProductoKeyPressed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        Articulo a = new Articulo();
        try{
            if(a.verificaCodigo(txtBuscaProducto.getText())){
                ProyectoIS2._Code = txtBuscaProducto.getText();
                confirmaEliminar = new Frm_ConfirmaEliminar();
                confirmaEliminar.setVisible(true);
                setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "Codigo Invalido... Ingrese uno de los "
                        + "que estan en la tabla", "", JOptionPane.WARNING_MESSAGE);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ingrese un codigo valido...!!!", "", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

