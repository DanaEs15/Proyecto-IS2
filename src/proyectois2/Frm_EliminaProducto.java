    public final void actualizarTabla() throws Exception{
    DefaultTableModel modelo = new DefaultTableModel();
        AccesoBD bd = new AccesoBD();
        ResultSet rs = bd.getTabla("SELECT codigo, cantidad, descripcion, precio FROM articulo");
        modelo.setColumnIdentifiers(new Object[]{"Código", "Cantidad", "Descripción", "Precio"});
        try{
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("codigo"), rs.getInt("cantidad"),
                rs.getString("descripcion"), rs.getDouble("precio")});
            }
            jtMuestraProductos.setModel(modelo);
        }
        catch(SQLException e){
            System.out.println(e);
        }
    }
