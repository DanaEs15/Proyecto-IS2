    private void txtCodigoaBuscarModificaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoaBuscarModificaKeyPressed
        // TODO add your handling code here
        AccesoBD bd = new AccesoBD();
        String[] Titulos = {"Código", "Nombre Fabricante"};
        String[] Registro = new String[50];

        String sql = "SELECT * FROM fabricante WHERE id_fabricante LIKE '%" + txtCodigoaBuscarModifica.getText()+ "%'"
        + "OR nombre_fabricante LIKE '%" + txtCodigoaBuscarModifica.getText() + "%'";

        model = new DefaultTableModel(null,Titulos);
        try {
            bd.conectaBD();
            Statement sta = bd.getConexion().createStatement();
            ResultSet rs= sta.executeQuery(sql);

            while(rs.next()){
                Registro[0] = rs.getString("id_fabricante");
                Registro[1] = rs.getString("nombre_fabricante");
                model.addRow(Registro);
            }
            jtMuestraFabricanteParaModificar.setModel(model);
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_txtCodigoaBuscarModificaKeyPressed

