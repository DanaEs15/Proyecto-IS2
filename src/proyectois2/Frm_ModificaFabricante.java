    private void actualizarTabla() throws Exception{
    DefaultTableModel modelo = new DefaultTableModel();
        AccesoBD bd = new AccesoBD();
        ResultSet rs = bd.getTabla("SELECT id_fabricante, nombre_fabricante FROM fabricante");
        modelo.setColumnIdentifiers(new Object[]{"Código", "Nombre Frabricante"});
        try{
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getInt("id_fabricante"), rs.getString("nombre_fabricante")});
            }
            jtMuestraFabricanteParaModificar.setModel(modelo);
        }
        catch(SQLException e){
            System.out.println(e);
        }
    }

