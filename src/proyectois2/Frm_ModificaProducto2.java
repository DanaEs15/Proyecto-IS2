    private void txtCodigoaBuscarModificaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoaBuscarModificaKeyPressed
        // TODO add your handling code here
        AccesoBD bd = new AccesoBD();
        String[] Titulos = {"Código", "Stock", "Descripcion", "Precio"};
        String[] Registro = new String[50];
        
        String sql = "SELECT * FROM articulo WHERE codigo LIKE '%" + txtCodigoaBuscarModifica.getText()+ "%'"
            + "OR stock LIKE '%" + txtCodigoaBuscarModifica.getText() + "%'"
            + "OR descripcion LIKE '%" + txtCodigoaBuscarModifica.getText() + "%'"
            + "OR precio LIKE '%" + txtCodigoaBuscarModifica.getText() + "%'";
        
        model = new DefaultTableModel(null,Titulos);
        try {
            bd.conectaBD();
            Statement sta = bd.getConexion().createStatement();
            ResultSet rs= sta.executeQuery(sql);
            
            while(rs.next()){
                Registro[0] = rs.getString("codigo");
                Registro[1] = rs.getString("stock");
                Registro[2] = rs.getString("descripcion");
                Registro[3] = rs.getString("precio");
                model.addRow(Registro);
            }
            jtMuestraProductosParaModificar.setModel(model);
        } catch (Exception ex) {
            
        }
    }//GEN-LAST:event_txtCodigoaBuscarModificaKeyPressed

