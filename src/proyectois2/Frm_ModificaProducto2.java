    public final void actualizarTabla() throws Exception{
    DefaultTableModel modelo = new DefaultTableModel();
        AccesoBD bd = new AccesoBD();
        ResultSet rs = bd.getTabla("SELECT codigo, stock, descripcion, precio FROM articulo");
        modelo.setColumnIdentifiers(new Object[]{"Código", "Stock", "Descripción", "Precio"});
        try{
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("codigo"), rs.getInt("stock"),
                rs.getString("descripcion"), rs.getDouble("precio")});
            }
            jtMuestraProductosParaModificar.setModel(modelo);
        }
        catch(SQLException e){
            System.out.println(e);
        }
    }

